// WHILE

var num = 0; // adiciona o valor antes de verificar
do {
    console.log(num);
    num++;
} while (num <= 6);

var num = 0; // verifica antes de adicionar o valor
while (num <= 6) {
    num++;
    console.log(num);
}
console.log("Fim");

